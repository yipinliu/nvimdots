local status_ok, lualine = pcall(require, "lualine")
if not status_ok then
    return
end

local config = {
    options = {
        theme = "onedark",
        component_separators = { left = ' |', right = '| ' }
    },
    sections = {
        lualine_c = {},
    },
}

-- Color for highlights
local colors = {
  yellow = '#ECBE7B',
  cyan = '#008080',
  darkblue = '#081633',
  green = '#98be65',
  orange = '#FF8800',
